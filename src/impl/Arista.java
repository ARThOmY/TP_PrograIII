package impl;

public class Arista {
    int ver1;
    int ver2;
    int peso;

    public Arista(int ver1, int ver2, int peso){

        this.ver1 = ver1;
        this.ver2 = ver2;
        this.peso = peso;
    }


    public int getVer1() {
        return ver1;
    }

    public int getVer2() {
        return ver2;
    }

    public int getPeso() {
        return peso;
    }
}
